package br.com.atividades;

public class OperadoresLogicos {
    public static void main(String[] args) {
        //not exemplo 01
        System.out.print(!( 10 == 10));

        //not exemplo 02
        System.out.print(!( 10 != 10));

        //and
        System.out.print( 10 < 20 && 10 > 5);

        // or
        System.out.print( 10 > 20 || 10 < 5);

    }
}
