package br.com.atividades;

public class OperadoresDecomparação {
    public static void main(String[] args) {
        //maior que
        System.out.println(10 > 10);

        //menor que
        System.out.println(15 > 10);

        //igualdade
        System.out.println(10 == 10);

        //diferença
        System.out.println(10 != 10);

        //Maior ou igual
        System.out.print(11 >= 10);

        //menor ou igual
        System.out.print(10 <= 10);

    }
}
